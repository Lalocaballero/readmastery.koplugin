-- Function to get ASCII art for an achievement
function AsciiArt.getSmall(achievement_id, unlocked)
    if not unlocked then
        return AsciiArt.small.locked or "[ ? ]"
    end
    return AsciiArt.small[achievement_id] or AsciiArt.small.locked or "[ ? ]"
end

function AsciiArt.getLarge(achievement_id, unlocked)
    if not unlocked then
        return AsciiArt.large.locked or "[ LOCKED ]"
    end
    return AsciiArt.large[achievement_id] or AsciiArt.large.locked or "[ ? ]"
end

return AsciiArt
